package com.gjn.easyapp.easyutils

object UnitObj {

    const val TIME_MINUTE = 60.toLong()
    const val TIME_HOUR = TIME_MINUTE * 60.toLong()
    const val TIME_DAY = TIME_HOUR * 24.toLong()
    const val TIME_WEEK = TIME_DAY * 7.toLong()
    const val TIME_MONTH = TIME_DAY * 30.toLong()
    const val TIME_YEAR = TIME_DAY * 365.toLong()

    const val SIZE_KB = 1024.toLong()
    const val SIZE_MB = SIZE_KB * 1024.toLong()
    const val SIZE_GB = SIZE_MB * 1024.toLong()
    const val SIZE_TB = SIZE_GB * 1024.toLong()

    const val SIZE_KB_2 = 1000.toLong()
    const val SIZE_MB_2 = SIZE_KB_2 * 1000.toLong()
    const val SIZE_GB_2 = SIZE_MB_2 * 1000.toLong()
    const val SIZE_TB_2 = SIZE_GB_2 * 1000.toLong()
}